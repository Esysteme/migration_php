<br>15 septembre 2004 : les jeunes ne sont toujours pas sevrés, et pourtant, il y a encore 3 œufs dans le nid. Le mâle est déplumé par endroit : est ce dû au stress, à trop de nourriture vivante, je l'ignore mais on aperçoit la graisse.
<br>Evidemment, cette nouvelle couvée se solde par un échec. Inutile de fatiguer la femelle pour rien donc j'arrête la reproduction. J'enlève le nid dans la journée et je le remets le soir pour dormir. Ainsi, il n'y a plus eu de ponte.
<br>
<br> <div align="center" class="sc">
