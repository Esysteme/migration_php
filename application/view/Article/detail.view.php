<br>J'ai remarqué, que les premiers jours, les parents nourrissent les jeunes avec la pâtée grasse (vu pour la première couvée car j'ignorais les naissances), un peu de nourriture vivante mais pas en grosse quantité, ensuite ils donnent des graines prédigérées après les huit premiers jours environ.
<br>
<br>Pour, le nourrissage de la deuxième couvée, j'ai donnée une plus grande quantité de nourriture vivante. Personnellement, pour les prochaines couvées, j'adopterai la première méthode, c'est à dire peu de nourriture vivante, plus de pinkies et moins de vers de farine.
<br>
<br><div align="center" class="titre">
      Conclusion :
    </div>
    
<br>Sur deux couples, un seul couple a reproduit, celui logé seul, dans une grande cage. L'autre couple, en volière, ne se reproduira pas à mon avis en collectivité ou alors très difficilement. 
<br>Il faut remarquer que dans mon cas, la femelle a repondu très rapidement après la sortie des jeunes du nid, cependant ces couvaisons successives semblent très compromises par la présence des jeunes de la première nichée. Il me semble que pour ménager la santé de la femelle, il vaut mieux espacer les pontes. Je l'ai fait en enlevant le nid dans la journée et en le remettant le soir dans sa fonction dortoir.
