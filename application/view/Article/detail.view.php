 <br>12 avril 2004 : 4 œufs dans le nid dont 2 fécondés.
 <br>
 <br>23 avril 2004 : Grande émotion, j'ai entendu des piaillements !
 <br>Pendant un instant, je me demande d'où provenaient ces piaillements, aucune naissance prévue, sauf... chez les Sénégali sanguin. Je suis très tentée de regarder dans le nid mais je résiste. J'entends à nouveau ces piaillements et cette fois, je repère bien leur origine : aucun doute, les Sénégali sanguin sont nés,  et moi qui  n'y croyais plus !
 <br>
 <br>25 avril 2004 : j'entends toujours les piaillements, donc les petits sont encore vivants. Les parents ne laissent jamais le nid seul, ils se relaient : quand l'un sort, l'autre rentre. Début d'après midi, les deux adultes sont hors du nid : la femelle est sortie pour manger et le mâle sort précipitamment lorsque je mets de la nourriture. Vite, j'en profite pour regarder dans le nid, je vois 2 œufs clairs et un oisillon énorme mais pas le 4 ème œuf, ni un second oisillon. Je ne pousse pas plus loin la recherche, vite je referme le nid.
 <br>
 <br>7 mai 2004 : Une sortie un peu forcée, pour le jeune Sénégali. 
 <br>Eh bien oui, je n'ai pas pu résister, j'ai regardé dans le nid et comme je ne voyais ni n'entendais rien, j'ai mis un doigt et hop!... un truc tout noir a fusé du nid comme une tornade en faisant couec, couec, couec, en sautillant dans tous les sens, et les parents un peu affolés ont repris en cœur : couec, couec,couec, un vrai concert ! Moi j'avais le cœur qui battait à toute vitesse car j'étais contente et émue mais aussi, je me demandais si je n'avais pas fait une énorme bêtise. Enfin, j'ai réussi à attraper l'oisillon et à le remettre dans le nid. Il est ressorti plusieurs fois, mais ne pouvant voler correctement, je l'ai remis à chaque fois. Il serait bien resté quelques jours de plus au nid, son plumage n'était pas terminé sous le ventre. Quelle émotion pour nous deux !
 <br>
