    </div>
<br>15 août 2004 : 2 jeunes de nés, le 3ème trouvé mort dans l'œuf qui était bêché : l'œuf était entre les fibres et le bois, il a peut être roulé.
<br>Les parents commencent à sortir ensemble du nid, de plus en plus, pour chercher la nourriture, il fait très chaud dans la pièce.
<br>
<br>17 août 2004 : baguage d'un jeune, le plus gros  
<br>
<br><div align="center" class="sc">
          <img src="http://passereaux.org/membres/articles/img/4-2005-03-28-MRsanguin5.jpg" alt="Spermophaga haematina" width="300" height="258" border="0">
      <br>  19 août 2004 : baguage du second
    </div>
    
<br>L'aîné ouvre les yeux, j'ai aperçu des graines dans son jabot. Ils piaillent l'après midi et le soir, mais ils sont silencieux le matin.
<br>
<br>Ce matin, en regardant dans le nid, les jeunes ouvrent grand le bec, surtout le plus petit qui semble moins gavé, je l'ai donc placé en premier. Je note une réelle différence de taille qui s'explique par le fait que les jeunes naissent sur plusieurs jours.
<br>
<br> <div align="center" class="sc">
            <img src="http://passereaux.org/membres/articles/img/4-2005-03-28-MRsanguin6.jpg" alt="Spermophaga haematina" width="300" height="376" border="0">
       <br> Découverte du monde, pour le plus téméraire
     </div>
     
<br>A la sortie du nid, le plus jeune a quelques petits problèmes de plumes, il ne peut pas voler correctement : ses plumes des ailes et de la queue sont trop courtes, il ne peut donc se percher ni rentrer seul dans le nid. Le jeune ne peut pas plus aller quémander à manger sur le perchoir, près des parents. Je dois donc le rentrer dans le nid tous les soirs.
<br>J'ai remarqué que les parents ne donnent pas à manger au jeune dans le nid ou très peu, alors je le perche ou je le pose sur la gamelle de nourriture.
<br>Au bout du second jour, vu ses cris, je complète avec trois seringues de bouillie par jour : matin, midi et soir. Quelques jours après, j'observe que le soir il a souvent le jabot plein, preuve d'un nourrissage naturel en journée, ce qui m'évite une complémentation. Je donne également des minis vers de farine, blancs et mous, sans tête, roulés dans probi-zyme, que le jeune avale par la queue. Si le ver de farine est un peu gros, je présente le côté tête, le jeune ne pouvant avaler le ver, il en aspire l'intérieur. Je nourris ainsi ce jeune pendant plus d'une semaine, en complément du nourrissage des parents. Je continue à le percher dans la journée et à le rentrer dans le nid le soir. Maintenant, il se perche et  il a rattrapé son retard sans problème.
<br>
