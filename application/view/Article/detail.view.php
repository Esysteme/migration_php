       <br> Frère et sœur
     </div>
     
<br>Les jeunes deviennent très beaux, et se révèlent être une femelle et un magnifique mâle (le petit déplumé).
<br>Le père est toujours aussi moche et je m'aperçois que la femelle est aussi déplumée, rien d'alarmant, c'est la mue. Cependant, la femelle se déplume de plus en plus, notamment sur le croupion, et une aile. Surprise, j'observe le jeune mâle en train de piquer sa mère. Je diminue fortement la nourriture vivante, je n'en donne qu'un jour sur deux et puis un jour sur trois et puis simplement deux fois par semaine. Je ne veux pas séparer les jeunes des parents, j'ai peur de renouveler mon erreur.
<br>
<br><div align="center" class="sc">
          <img src="http://passereaux.org/membres/articles/img/4-2005-03-28-MRsanguin9.jpg" alt="Spermophaga haematina" width="450" height="284" border="0">
       <br> La famille quelque peu éprouvée par ses rejetons...
    </div>
    
<br>Le problème ne s'aggrave pas !
<br>Au début  janvier, je sèvre les jeunes des parents pour que ceux-ci se remplument. Actuellement, les parents sont devenus très beaux en retrouvant toutes leurs plumes.
<br>J'ignore exactement pourquoi ce jeune mâle avait tendance à plumer ses parents car il ne le fait pas avec sa sœur. En tout cas, ils n'ont pratiquement plus de nourriture vivante et peu de pâtée. Ils sont devenus très familiers, quand je rentre dans la  pièce, ils s'approchent et attendent que je leur donne un ver de farine à la main, la femelle est la première servie, le mâle est plus hésitant, c'est pourtant lui que j'ai  manipulé pour le nourrir.
<br>

<br><h3>
      La nourriture pendant le nourrissage
    </h3>
    
<br>
<br>Les parents ont eu à leur disposition une grande variété de graines petites et grosses, des fruits ou légumes, très peu de nourriture vivante pour la première couvaison, un peu plus pour la seconde, de la pâtée grasse Bévo avec des protéines végétales ou de la pâtée Vitajeune de Francodex, de la pâtée indigène et exotique dans un autre récipient, et bien sûr du millet rouge en grappes.
<br>
<br><div align="center" class="sc">
          <img src="http://passereaux.org/membres/articles/img/4-2005-03-28-MRsanguin10.jpg" alt="Spermophaga haematina" width="450" height="295" border="0">
      <br>Mais oui, la fille (à droite) est plus forte que sa mère !
