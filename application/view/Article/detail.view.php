<br>8 mai 2004 : alors que je pensais qu'il lui manquait quelques jours d'autonomie, le jeune sort et rentre cependant seul dans le nid, un peu maladroit, plusieurs fois dans la journée.
<br>
<br>10 mai 2004 : le jeune se perche comme un grand, retourne au nid quand il le désire. C'est fantastique de l'observer, je découvre plein de choses, par exemple, que ce jeune retourne dans le nid, pour s'y réfugier mais aussi pour y dormir avec ses parents le soir.
<br>
<br>13 mai 2004 : Le jeune commence à prendre des couleurs. Il dort toujours avec ses parents qui continuent patiemment à le nourrir. La femelle a entamé une nouvelle couvaison mais qui se soldera par un échec, ainsi que les suivantes. C'est sûrement dû à la présence du jeune qui perturbe la couvaison (œufs déplacés...) mais je ne peux pas encore sevrer ce jeune
<br>Je n'arrive pas à définir, ni à confirmer le sexe de ce juvénile qui a un ongle blanc, un défaut sans doute. Quand il est effrayé (à cause de son père), il se précipite dans le nid, en bousculant les œufs. D'un vol direct de 120 cm, impressionnant, un peu déséquilibré, en passant entre les branches, il rentre au nid, ou s'arrête à l'entrée. On perçoit déjà bien la puissance de ce vol aussi bien horizontalement que verticalement.
<br>
<br>1 juin 2004 : le jeune Sénégali sanguin s'affirme de plus en plus en couleurs. Au début, il était tout noir, je pensais que c'était un mâle (à vrai dire, nous étions plusieurs à le croire !). Maintenant sa poitrine commence à rougir, sa tête également, on voit apparaître du bleu sur son bec, le bout du bec devient un peu rouge, sous son ventre pointent quelques plumes blanches... Enfin voilà, ce jeune commence à ressembler de plus en plus à une femelle. Je suis folle de joie car c'est ce que j'avais espéré.
