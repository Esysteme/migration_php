<br>  Après quelques années de galère, à la recherche d'une femelle Sénégali sanguin, j'ai saisi une occasion, et trois oiseaux (une femelle et deux mâles) sont arrivés à la maison début novembre 2003. J'en profite pour remercier la personne qui m'a trouvé et convoyé ces oiseaux. Après une période de quarantaine, une visite chez le vétérinaire, j'ai été amené à traiter à l'Ivomec pour une présence de nécroses aux pattes avec hémorragie conséquence d'une gale non traitée. Ces oiseaux se sont vite adaptés, sans traitement aux antibiotiques (sauf mention précédente), et de plus ont manifesté des envies de nicher rapidement. Dès début janvier, un couple s'est formé entre le mâle le plus " moche " et la seule femelle du trio. Ils manifestaient  une envie de nicher de plus en plus pressante, ouvrant le nid dont j'avais obstrué temporairement l'entrée avec du papier, décortiquant des feuilles des branches pour le garnir. J'ai réussi à contenir cette envie jusqu'à fin mars, en refermant le nid, en défaisant le nid  J'avais retiré le second mâle par prudence. Dès fin mars, le nid laissé accessible fut très rapidement rempli par des matériaux et la femelle y déposa très vite ses oeufs. 
