<br>Cette femelle est devenue très, très belle, malheureusement sa vie a basculé au mois de juillet, lorsque j'ai décidé de la séparer de ses parents pour avoir une chance d'avoir une nouvelle couvaison réussie.  Cette femelle est morte deux jours après... je m'en suis voulue d'avoir commis cette erreur.
<br>
<br>Heureusement, la saison n'était pas achevée !
<br>
<br>4 août 2004 : 4 œufs dont 3 fécondés.
<br>
<br><div align="center" class="sc">
